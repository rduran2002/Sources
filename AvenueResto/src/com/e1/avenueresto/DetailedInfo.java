package com.e1.avenueresto;




public class DetailedInfo 
{
	
	
	public Double Price;
	public String Serving;
	public Double Discount;
	public Boolean IsAvailable;
	public Boolean AvailableFrom;
	public Boolean AvailableTo;
	public Boolean PreparationInMin;
	public Menu Menus;
	public Double getPrice() {
		return Price;
	}
	public void setPrice(Double price) {
		Price = price;
	}
	public String getServing() {
		return Serving;
	}
	public void setServing(String serving) {
		Serving = serving;
	}
	public Double getDiscount() {
		return Discount;
	}
	public void setDiscount(Double discount) {
		Discount = discount;
	}
	public Boolean getIsAvailable() {
		return IsAvailable;
	}
	public void setIsAvailable(Boolean isAvailable) {
		IsAvailable = isAvailable;
	}
	public Boolean getAvailableFrom() {
		return AvailableFrom;
	}
	public void setAvailableFrom(Boolean availableFrom) {
		AvailableFrom = availableFrom;
	}
	public Boolean getAvailableTo() {
		return AvailableTo;
	}
	public void setAvailableTo(Boolean availableTo) {
		AvailableTo = availableTo;
	}
	public Boolean getPreparationInMin() {
		return PreparationInMin;
	}
	public void setPreparationInMin(Boolean preparationInMin) {
		PreparationInMin = preparationInMin;
	}
	public Menu getMenus() {
		return Menus;
	}
	public void setMenus(Menu menus) {
		Menus = menus;
	}
	
	
	
}
